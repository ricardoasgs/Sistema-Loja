 public void mostraRegistro(){
     try{
            rdbm.setSelected(false);
            rdbf.setSelected(false);
            s="";
            tfcodigo.setText(conexao_cli.resultset.getString("codigo"));
            tfnome.setText(conexao_cli.resultset.getString("nome"));
            tfendereco.setText(conexao_cli.resultset.getString("endereco"));
            tfnumero.setText(conexao_cli.resultset.getString("numero"));
            tfcomplemento.setText(conexao_cli.resultset.getString("complemento"));
            tfbairro.setText(conexao_cli.resultset.getString("bairro"));
            tfcidade.setText(conexao_cli.resultset.getString("cidade"));
            cboestado.setSelectedItem(conexao_cli.resultset.getString("estado"));
            tffone.setText(conexao_cli.resultset.getString("fone"));
            tfemail.setText(conexao_cli.resultset.getString("email"));
            s=(conexao_cli.resultset.getString("sexo"));
            if (s.equals("m"))
            {
                rdbm.setSelected(true);
            }
            else if (s.equals("f"))
            {
                rdbf.setSelected(true);
            }
     
 }
     catch (SQLException erro){
        JOptionPane.showMessageDialog(null,"erro na conexao com o SQL"+ erro);  
     }
 }
