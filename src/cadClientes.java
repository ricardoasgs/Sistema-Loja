    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
       //validação para campo de texto preenchimento obrigatório
        if(tfcodigo.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"Código obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfcodigo.requestFocus();
                }
       else if(tfnome.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"nome obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfnome.requestFocus();
                }
       else if(tfendereco.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"endereço obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfendereco.requestFocus();
                }
        else if(!rdbm.isSelected()&&!rdbf.isSelected())
                {
             JOptionPane.showMessageDialog(null,"sexo obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                rdbm.requestFocus();
                }
         else if(tfnumero.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"numero obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfnumero.requestFocus();
                }
         else if(tfbairro.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"bairro obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfbairro.requestFocus();
                }
         else if(tfcidade.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"cidade obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfcidade.requestFocus();
                }
         else if(cboestado.getSelectedIndex() ==-1)
                {
             JOptionPane.showMessageDialog(null,"estado obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                cboestado.requestFocus();
                }
       else if(tffone.getText().equals("(  )    -    "))
                {
             JOptionPane.showMessageDialog(null,"fone obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tffone.requestFocus();
                }
       
        else if(tfemail.getText().equals(""))
                {
             JOptionPane.showMessageDialog(null,"email obrigatório","Alerta",JOptionPane.INFORMATION_MESSAGE);
                tfemail.requestFocus();
                }
      
        else
        {
        
        try
        {
            String sql = "insert into clientes (codigo, nome,sexo, endereco , numero, complemento,bairro,cidade,estado,fone,email) Values('"+tfcodigo.getText() +"' ,'" +tfnome.getText()+"','"+s+"','"+tfendereco.getText()+"' ,'"+tfnumero.getText()+"','"+tfcomplemento.getText()+"','"+tfbairro.getText()+"','"+tfcidade.getText()+"','"+cboestado.getSelectedItem()+"','"+tffone.getText()+"','"+tfemail.getText()+"')";
            //executa sql,para atualização do banco usar o executeUpdate
            conexao_cli.statement.executeUpdate(sql);
             JOptionPane.showMessageDialog(null,"Dados gravados com sucesso"); 
             //atualiza resultset,para apenas consulta(comando select) utilizar o metodo executasql
             conexao_cli.executaSql("select * from clientes");
             conexao_cli.resultset.last();
             //chama o metodo mostraRegistro, que exibe o registro do banco no cambo texto
        }
        catch (SQLException erro)
        {
          JOptionPane.showMessageDialog(null,"erro ao salvar"+ erro);     
        }
        }
    }//GEN-LAST:event_btnsalvarActionPerformed

    private void btnexcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnexcluirActionPerformed
       try
       {
        String msg = "Excluir cliente " + tfnome.getText() + "?";
        int  opçao = JOptionPane.showConfirmDialog(null, msg, "Atenção", JOptionPane.YES_NO_OPTION);
        //caso opção igual a sim
        if (opçao == JOptionPane.YES_OPTION)
        {
            String sql = "delete from clientes where codigo = " + tfcodigo.getText();
            conexao_cli.statement.executeUpdate(sql);
            //atualiza resultset
            conexao_cli.executaSql("select * from clientes ");
            //posiciona no primeiro registro
            conexao_cli.resultset.first();
            //chama metodo que exibe registro no campo de texto
            mostraRegistro();
        }
        
       }
        catch (SQLException erro)
        {
          JOptionPane.showMessageDialog(null,"erro ao excluir"+ erro);     
        }
    }//GEN-LAST:event_btnexcluirActionPerformed

